<h1>About</h1>

	<p><b>What is a webring?</b> A webring is a directory of websites that link to each other within a circular structure. In the early days of the internet, webrings were how people connected and found each other through a shared common interest based on the subject of the webring. Within the recent resurgence of passionate internet decentralization, this webring was created to help connect website owners specificially in fandom, especially as fandom itself has become reliant on centralized social media in recent years.</p>

	<p>If you're in fandom and have a personal website, consider joining! And if you're in fandom but don't have a personal website <i>yet</i>, consider creating one &mdash; and check out the <a href="ideas">ideas & resources page</a> if you need help.</p>

	<p><b>What qualifies as being in fandom?</b> I made this webring with fans like myself in mind &mdash; writers, readers, and artists of the slashy shipper variety. But I know fandom is bigger than this and I don't aim to be exclusionary. Overall, this webring is welcome to anyone who considers themselves in fandom in some way, particularly as someone who likes to create. To me, being in fandom means having an interest in exploring in the transformation of existing media, whether it's out of love or horniness or a secret tenth option. This webring aims to bring together fans who are similarly passionate in this regard.</p>

	<p>So if you're interested in being a part of this webring, <a href="join.html">check out the rules and join</a>!</p>


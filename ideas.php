	<h2>Checklist</h2>

	<ol>
		<li>Decide where you would like your website to exist: Neocities, Nekoweb, another host, or somewhere else.</li>
		<li>Create a username/website to be the URL of your website. You usually cannot change this without moving your website entirely, so keep that in mind when making your account.</li>
		<li>Create the front page of your website: the first thing people will see when they visit your site.</li>
		<li>Create subpages: "about" you, contact/social media, your fandoms, ships, favorite characters, likes, squicks.</li>
		<li>Share your fanworks, creations, what you've participated in, and anything else that you'd like to put on your corner of the internet.</li>
		<li>Make your website look pretty with CSS, themes, pictures, colors, and codes.</li>
		<li>Find or make graphics for your site. Maybe an <a href="https://indieweb.org/88x31">88x31 button</a>?</li>
		<li>Give the URL to all your friends and make them look at your cool website.</li>
		<li>Join <a href="https://brisray.com/web/webring-list.htm">webrings</a>, directories, and <a href="https://thefanlistings.org">fanlistings</a> of the things you like. </li>
		<li>Collect <a href="https://capstasher.neocities.org/88x31collection-page1">stamps</a>, <a href="https://adriansblinkiecollection.neocities.org/">blinkies</a>, <a href="https://y2k.neocities.org/collections">buttons</a>; create an aesthetic wall; take personality tests and share your results; create a type of fanwork you've never created before to share; write your thoughts about being one of millions on the internet, in fandom, and in the world.</li>
		<li>Continue making updates, changes, and new decisions for your website as you experience the passage of time.</li>
		<li>Profit (self-fulfillment).</li>

	</ol>


	<h1>Ideas & Resources</h1>

	<p>Not everyone in fandom has experience with creating or managing a website of their own. These days it feels more common to participate in fandom through the use of existing platforms: Tumblr, Twitter/Bluesky, LiveJournal/Dreamwidth, AO3 &mdash; through making accounts so that we can attribute a name to our works. But a website is beyond that. Instead of just creating an identity, we also create a space that is intrinsic on our identity.</p>

	<p>But even if the idea of having a website sounds appealing, you might ask yourself, what would I even put on it?</p>

	<p>So I've gathered here a non-exhaustive list of ideas in case you would like inspiration. No credit is required; these are merely examples of things that would be fun for me to find on a personal fannish website. Feel free to "steal" as many as you'd like or merely get inspired by them and do something different. All I'd ask is for you to do what brings you joy for what you want your website to be.</p>

